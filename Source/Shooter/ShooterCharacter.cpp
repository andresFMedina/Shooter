void AShooterCharacter::ShootBullet()
{
	const USkeletalMeshSocket* BarrelSocket = EquippedWeapon->GetItemMesh()->GetSocketByName("BarrelSocket");
	if (BarrelSocket)
	{
		const FTransform SocketTransform = BarrelSocket->GetSocketTransform(GetMesh());
		if (MuzzleFlash)
		{
			UGameplayStatics::SpawnEmitterAtLocation(GetWorld(), MuzzleFlash, SocketTransform);
		}

		FHitResult BeamHitResult;
		bool bBeamEnd = GetBeamEndLocation(SocketTransform.GetLocation(), BeamHitResult);
		if (bBeamEnd)
		{
			if (BeamHitResult.GetActor())
			{
				IBulletHit* BulletHitInterface = Cast<IBulletHit>( BeamHitResult.GetActor());
				if (BulletHitInterface)
				{
					BulletHitInterface->BulletHitted_Implementation(BeamHitResult, this, GetController());
				}

				AEnemy* HitEnemy = Cast<AEnemy>(BeamHitResult.GetActor());
				if (HitEnemy)
				{
					float DamageToApply = EquippedWeapon->GetDamage();
					bool bisHeadShot = false;
					if (*BeamHitResult.BoneName.ToString() == HitEnemy->GetHeadBone())
					{
						DamageToApply = EquippedWeapon->GetHeadShotDamage();
						bisHeadShot = true;
					}
					UGameplayStatics::ApplyDamage(
						BeamHitResult.GetActor(),
						DamageToApply,
						GetController(),
						this,
						UDamageType::StaticClass());

					HitEnemy->ShowHitNumber(DamageToApply, BeamHitResult.Location, bisHeadShot);
				}
			}
			else
			{
				if (ImpactParticles)
				{
					UGameplayStatics::SpawnEmitterAtLocation(GetWorld(), ImpactParticles, BeamHitResult.Location);
				}
			}
			

			if (BeamParticles)
			{
				UParticleSystemComponent* Beam = UGameplayStatics::SpawnEmitterAtLocation(GetWorld(), BeamParticles, SocketTransform);
				if (Beam)
				{
					Beam->SetVectorParameter(FName("Target"), BeamHitResult.Location);
				}
			}
		}
	}
}

